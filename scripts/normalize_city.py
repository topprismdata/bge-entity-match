def normalize_city(city: str) -> str:
    """将提取的城市名标准化为候选库使用的地级行政区划名。

    处理顺序：
    1. 空值/垃圾值 → 空字符串
    2. 自治州残缺名修复
    3. 省名前缀去除
    4. 双'市'后缀去除（青岛市市→青岛市）
    5. city+区/县+市 模式清理（广州市番禺区市→广州市）
    6. 县级市→地级市映射
    7. 补'市'后缀尝试匹配
    """
    if not isinstance(city, str) or not city.strip():
        return ''

    c = city.strip()

    # 1. 过滤垃圾城市名（非行政区的中文串）
    garbage_patterns = ['超市', '市场', '广场', '大厦', '商城', '中心', '综合',
                        '门市', '城市', '农贸', '菜市', '集市', '商贸', '装饰',
                        '眼镜', '超市', '生鲜', '配件', '负一', '一楼', '小区',
                        '茶叶', '副食', '钟表', '华联', '大润发', '永辉', '佳惠',
                        '宝龙', '光彩', '收件', '地址', '帮忙', '号']
    # 垃圾：以'市'结尾且包含非城市关键词
    if c.endswith('市') and len(c) > 4:
        has_garbage = any(p in c for p in garbage_patterns)
        if has_garbage:
            return ''
    # 垃圾：不以'市/州/盟/地区'结尾且不是已知的行政区
    valid_suffixes = ('市', '州', '盟', '地区')
    if not c.endswith(valid_suffixes) and len(c) <= 3:
        short_garbage = {'县', '东市', '山市', '肥市', '枣市', '州市',
                         '阳市', '安市', '晋市', '遵市', '商市', '鸡市',
                         '都市', '节市', '新发地市', '州市'}
        if c in short_garbage:
            return ''
    # 垃圾：以'号'开头 (地址编号误提取)
    if c.startswith('号') and len(c) <= 6:
        return ''
    # 垃圾：纯垃圾词
    pure_garbage = {'永辉超市', '邻你超市', '爱家超市', '百姓超市', '玖多超市',
                    '生鲜超市', '汇工眼镜', '佳惠超市', '生鲜市', '层超市',
                    '号门市', '间门市'}
    if c in pure_garbage:
        return ''
    # 垃圾：含'市'但明显是地址片段(含街道/镇/路/县+地名)
    import re as _re
    if _re.search(r'(街道|镇|路|乡|村|小区|一楼|负一|铺|院|苑|楼)', c):
        return ''

    # 1b. 过滤地址片段误提取（如 "易试验区金华市"、"自由贸易试验区金华市"）
    addr_fragments = ['试验区', '开发区', '自贸区', '高新区', '经济区', '合作区']
    for frag in addr_fragments:
        idx = c.find(frag)
        if idx >= 0:
            rest = c[idx + len(frag):]
            if rest:
                c = rest
                break

    # 2. 自治州/盟残缺名修复
    if c in AUTONOMOUS_FIXES:
        return AUTONOMOUS_FIXES[c]

    # 3. 省名前缀去除
    for prov in PROVINCE_PREFIXES:
        if c.startswith(prov) and c != prov + '市' and len(c) > len(prov):
            rest = c[len(prov):]
            if rest:
                c = rest
                break

    # 4. 双'市'后缀去除（青岛市市→青岛市）
    while c.endswith('市市'):
        c = c[:-1]

    # 5. city+区/县/市 模式清理（广州市番禺区市→广州市, 温州市乐清市→温州市）
    import re
    # 5a. XX区YY市 pattern → strip district prefix (白塔区辽阳市→辽阳市)
    m = re.match(r'^.+?区(.+市)$', c)
    if m:
        inner = m.group(1)
        if len(inner) >= 3 and inner.endswith('市'):
            c = inner
    # 5b. city+区/县 pattern (广州市番禺区市→广州市)
    m = re.match(r'^(.+?市)(.+?[区县市].*)$', c)
    if m:
        potential_city = m.group(1)
        if potential_city.endswith('市') and len(potential_city) <= 5:
            c = potential_city

    # 6. 县级市→地级市映射
    if c in COUNTY_TO_PREFECTURE:
        return COUNTY_TO_PREFECTURE[c]

    # 7. 不以'市'结尾的 → 尝试加'市'后映射
    if not c.endswith('市') and not c.endswith('地区') and not c.endswith('州') and not c.endswith('盟'):
        candidate = c + '市'
        if candidate in COUNTY_TO_PREFECTURE:
            return COUNTY_TO_PREFECTURE[candidate]

    return c


